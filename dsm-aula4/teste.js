// Função que soma dois números
function somar(a, b) {
    return a + b;
  }
  
  // Valores para teste
  const numero1 = 5;
  const numero2 = 3;
  
  // Calcula a soma
  const resultado = somar(numero1, numero2);
  
  // Exibe o resultado no console
  console.log(`A soma de ${numero1} e ${numero2} é igual a ${resultado}`);